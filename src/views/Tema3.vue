            p La Ley 1328 de 2009, conocida como la Reforma Financiera o el Estatuto del Consumidor Financiero, establece los principios y derechos de los usuarios del sistema financiero y las obligaciones que las entidades vigiladas deben cumplir en su relación con ellos. Para el gestor de cartera, esta norma define el marco de actuación durante todo el proceso de cobranza, desde el primer contacto hasta el cierre de la gestión.

